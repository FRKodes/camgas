	<h2>Calentadores de agua (boilers), estufas y calefactores.</h2>
	<p>
		Los boilers y calefactores de gas no deben instalarse en cocinas, patios cerrados o baños, ya que durante su uso emiten gases tóxicos. <br><br>

		Al boiler, estufa y demás aparatos que consumen Gas L.P., se deben utilizar y dar mantenimiento de acuerdo con las instrucciones del fabricante, de esta forma se conservarán en óptimas condiciones de funcionamiento.<br><br>

		Se debe verificar que los pilotos de los aparatos de consumo, cuando cuente con ellos, estén siempre encendidos.<br><br>

		Mantenga la estufa, calefactor y boiler en buenas condiciones físicas, solicite a personal calificado la revisión de sus perillas, válvulas, pilotos y demás accesorios.<br><br>

		Si un aparato no va a utilizarse durante mucho tiempo, cierre el suministro de gas.
	</p>	
