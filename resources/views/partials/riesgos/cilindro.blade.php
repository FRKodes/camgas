	<h2>Recipiente transportable (cilindro) de gas L.P.</h2>
	<p>
		Verifique que los Cilindros se encuentren en buenas condiciones físicas. Al recibirlos compruebe que no estén abollados, su base esté desprendida o tenga fuga. <br><br>

		No exponga el Cilindro al fuego o a calentamientos artificiales.<br><br>

		No voltee o acueste el Cilindro.<br><br>

		Utilice siempre un regulador de presión en la válvula o conexión del Cilindro.<br><br>

		Las tuercas de las conexiones que unen los Cilindros al regulador, pueden dañarse debido al uso, por ello hay que vigilar su condición física y reemplazarlas cuando se deterioren.<br><br>

		No perfore, golpee o utilice los Cilindros para fines distintos al almacenamiento de Gas L.P.<br><br>

		En algunos casos el comercializador le ofrece el servicio de entrega a domicilio, lo cual se efectúa a través de vehículos adecuados.<br><br>

		Si requiere transportarlo en su propio vehículo, debe hacerlo en el espacio destinado a la carga o en la cajuela, nunca en el área de pasajeros. <br><br>
		Tome las medidas necesarias para que el Cilindro se mantenga vertical, es decir, con la válvula hacia arriba.<br><br>

		Evite olvidar el Cilindro en el vehículo, una vez que llegue a su destino deberá retirarlo del automóvil y colocarlo en un lugar ventilado.
	</p>
