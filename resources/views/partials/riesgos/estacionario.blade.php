	<h2>Tanque estacionario</h2>
	<p>
		El Tanque Estacionario debe examinarse ultrasónicamente a partir de los diez años de su fabricación. Así se determinará si es apto para continuar en servicio. Esta valoración debe realizarse por una persona aprobada por la Secretaría de Energía. <br><br>

		Para evitar la corrosión exterior debe estar siempre pintado.<br><br>

		Es necesario reemplazar sus válvulas cada cinco años.<br><br>

		No exponga el Tanque Estacionario al fuego o a calentamientos artificiales.<br><br>

		INSTALACIÓN DE CILINDROS Y TANQUES ESTACIONARIOS

		Los Cilindros y Tanques Estacionarios deben instalarse a la intemperie y en lugares ventilados, así como a una distancia mínima de 3 metros de elementos que generen flama o chispas.<br><br>

		Colocarse sobre piso firme y nivelado, en un lugar de fácil acceso, sin humedad y alejado del paso de vehículos y personas. No deben usarse como base para otros objetos.<br><br>

		Al instalar los Cilindros, de ser posible, sujételos a la pared para que no estén sueltos.<br><br>

		Cuando se tengan dos o más Cilindros, la distancia mínima entre ellos debe ser 60 cm.<br><br>

		La distancia mínima entre un Cilindro y un Tanque Estacionario debe ser 1.5 metros.
	</p>	
