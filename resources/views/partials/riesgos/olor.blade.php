	<h2>¿Percibe olor a gas?</h2>
	<p>
		Apague inmediatamente cigarrillos u otras fuentes de flama o chispa. No encienda luces, teléfonos celulares u otros aparatos, ya que el fuego o las chispas pueden ocasionar una ignición. <br><br>

		Cierre la válvula de la fuente de suministro de Gas L.P. ya sea del Tanque Estacionario o del Cilindro, si es seguro hacerlo. Para cerrar la válvula, gírela en el sentido de las manecillas del reloj.<br><br>

		Todas las personas deben alejarse del área o construcción en donde se sospeche que exista una fuga de gas.<br><br>

		Reporte la fuga a su distribuidor de Gas L.P., al servicio de supresión de fugas, departamento de bomberos o protección civil de su localidad.<br><br>

		No regrese a la construcción o área hasta que su distribuidor de Gas L.P. o las autoridades determinen que es seguro hacerlo.<br><br>

		Si en su Tanque Estacionario o Cilindro, el Gas L.P. se escapa por la válvula de seguridad, es posible que tenga mucha presión interna, en ese caso, puede mojarlo con agua fría. No obstruya la válvula con pasta de jabón u otro elemento y repórtelo a la compañía de Gas L.P., servicio de supresión de fugas, protección civil o bomberos de su localidad.
	</p>	
