	<h2>¿Cómo evitar fugas de gas?</h2>
	<p>
		Para encender cualquier aparato a gas, siga las instrucciones del fabricante. <br><br>

		Compruebe regularmente que no haya fugas en las conexiones y válvulas en la instalación, mediante la aplicación de agua jabonosa.<br><br>

		Previo a salir de su domicilio, revise siempre que las llaves de la estufa, calentador de agua (boiler) o calefactor, estén cerradas, y en caso de contar con pilotos, que éstos se encuentren encendidos.<br><br>

		Si la flama del piloto de una estufa, boiler o calefactor, repetidamente se apaga o es complicado de encender, puede tenerse un problema de seguridad. No trate de arreglarlo usted mismo, contacte a un técnico calificado.<br><br>

		No intente modificar o reparar válvulas, reguladores, conexiones u otros accesorios de estufas y demás aparatos, así como los Cilindros o Tanques Estacionarios, contacte a un técnico calificado.
	</p>	
